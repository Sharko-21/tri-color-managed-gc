/// Высокоуровневый снимок (DTO) объекта, выступающий мостом между безопасным кодом приложения
/// и низкоуровневым представлением в виде сырых байт кучи.
///
/// Используется для сериализации (при записи объекта в кучу) и десериализации (при чтении из нее).
#[derive(Debug, Clone)]
#[allow(dead_code)]
pub struct ManagedObject {
    /// Идентификатор объекта.
    pub id: ObjectId,
    /// Список логических связей (указателей) на другие управляемые объекты.
    pub references: Vec<ObjectId>,
    /// Сырой массив байт, представляющий пользовательские данные объекта.
    pub payload: Vec<u8>,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum AllocError {
    OutOfMemory,
    InvalidPointer,
}